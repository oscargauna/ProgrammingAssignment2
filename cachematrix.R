## This program implements a function to cache the inverse of a matrix so it only has to be calculated once.

## This function creates a special vector of functions to cache the inverse of a matrix

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinv <- function(solve) m <<- solve
        getinv <- function() m
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)
}


## This functions returns the inverse of a matrix. It calls the makeCacheMatrix function to see if the inverse has been calculated.
## If it has been calculated, it returns the cached inverse matrix. If not, it calculates it.

cacheSolve <- function(x, ...) {
        m <- x$getinv()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinv(m)
        m
}
